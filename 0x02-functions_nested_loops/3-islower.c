#include "main.h"

/**
 * _islower - function that checks for lowercase character.
 * @c: charactor being difined
 *
 * Return: 1 if c is lowercase, return 0 otherwise
 *
 **/

int _islower(int c)
{	
	char alphabet;
	_putchar(c) = alphabet;

	//if ('z' >= c || 'a' <= c)
	if ('z' >= alphabet || 'a' <= alphabet)
	{
		return (1);
	}
	else
	{
		return (0);
	}
}
